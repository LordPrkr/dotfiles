function renderLeonard(theme: Theme): string[] {
	const orange = (text: string) => theme.fg("accent", text);
	const muted = (text: string) => theme.fg("muted", text);
	const dim = (text: string) => theme.fg("dim", text);

	return [
		...LEONARD_LINES.map(orange),
		`${muted(" Leonard the Orange")} ${dim(`v${VERSION}`)}`,
		`${dim(" wise • old • mischievous")}`,
	];
}

export default function (pi: ExtensionAPI) {
	pi.on("session_start", async (_event, ctx) => {
		if (!ctx.hasUI) return;

		ctx.ui.setHeader((_tui, theme) => ({
			render(_width: number): string[] {
				return renderLeonard(theme);
			},
			invalidate() {},
		}));
	});

	pi.registerCommand("builtin-header", {
		description: "Restore Pi's built-in startup header",
		handler: async (_args, ctx) => {
			ctx.ui.setHeader(undefined);
			ctx.ui.notify("Built-in header restored", "info");
		},
	});
}
